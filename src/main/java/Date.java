public class Date {

    private int day;
    private int month;
    private int year;
    private int time;
    private boolean am;

    public Date(int day, int month, int year, int time, boolean am) {
        this.day = day;
        this.month = month;
        this.year = year;
        this.time = time;
        this.am = am;
    }
}
